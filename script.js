let currentQuestion = 0;
let currentAnswer = 0;
let score = 0;
let filteredQuestions = [];
let userAnswers = [];
function startQuiz() {
    let username = document.getElementById("username");
    let topic = document.getElementById("topic");
    if (username.value == "") {
        Swal.fire({
            icon: 'error',
            title: 'Oops...',
            text: 'Please enter a username.'
        });
        return;
    }
    if (topic.value == "") {
        Swal.fire({
            icon: 'error',
            title: 'Oops...',
            text: 'Please select a topic.'
        });
        return;
    }
    filteredQuestions = questions[topic.value];
    console.log(filteredQuestions);
    document.getElementById("main_container").style.display = "none";
    document.getElementById("quiz_container").style.display = "block";

    loadQuestions();
}
function loadQuestions() {
    let question = document.getElementById("question");
    let optionList = document.getElementById("option_list");
    let current = document.getElementById("current");
    let total = document.getElementById("total");
    optionList.innerHTML = ""

    let currentQuestionObject = filteredQuestions[currentQuestion];

    question.innerText = currentQuestionObject.question;

    current.innerText = currentQuestion + 1;
    total.innerText = filteredQuestions.length;

    for (var i = 0; i < currentQuestionObject.option.length; i++) {
        let optiondiv = document.createElement("div");
        optiondiv.setAttribute("class", "optiondiv");
        let inputElement = document.createElement("input");
        inputElement.type = "radio"
        inputElement.name = "option"
        inputElement.value = currentQuestionObject.option[i]

        let labelElement = document.createElement("label");
        labelElement.innerText = currentQuestionObject.option[i];
        labelElement.setAttribute("class", "text-light ms-2");

        optiondiv.appendChild(inputElement);
        optiondiv.appendChild(labelElement);
        optiondiv.addEventListener('click', function () {
            inputElement.checked = true;
        });
        optionList.appendChild(optiondiv);
        console.log(inputElement)
    }

}

function nextQuest() {
    let selectedOption = document.querySelector('input[name="option"]:checked')
    console.log(selectedOption);

    if (!selectedOption) {
        Swal.fire({
            icon: 'error',
            title: 'Oops...',
            text: 'Please select an option.'
        });
        return;
    }

    userAnswers[currentQuestion] = selectedOption.value;

    if (selectedOption.value == filteredQuestions[currentQuestion].answer) {
        score++
    }
    currentQuestion++
    if (currentQuestion < filteredQuestions.length) {
        loadQuestions()
    } else {
        showResult()
    }

}

function showResult() {
    document.getElementById("quiz_container").style.display = "none";
    document.getElementById("result_container").style.display = "block";
    let name = document.getElementById("name");
    let topicdisplay = document.getElementById("topicdisplay");
    let remarks = document.getElementById("remarks");
    let username = document.getElementById("username").value;
    let textElement = document.getElementById("resultText");
    textElement.innerText = `Score: ${score} / ${filteredQuestions.length}`;
    name.innerText = username;
    topicdisplay.innerText = `Topic: ${topic.value}`;
    let percentage = (score / filteredQuestions.length) * 100;
    if (percentage >= 70) {
        remarks.innerText = "Excellent 🔥🎉🌟";
    } else if (percentage >= 50) {
        remarks.innerText = "Good 👍😊";
    } else {
        remarks.innerText = "Needs Improvement 😕💪";
    }
    console.log(userAnswers);
}

function displayAnswers() {
    document.getElementById("result_container").style.display = "none";
    document.getElementById("answers_container").style.display = "block";
    loadAnswers();
}

function loadAnswers() {
    let answer_question = document.getElementById("answer_question");
    let answer_option_list = document.getElementById("answer_option_list");
    let current_answer = document.getElementById("current_answer");
    let total_answer = document.getElementById("total_answer");
    let user_answer_display = document.getElementById("user_answer_display");
    let correct_answer_display = document.getElementById("correct_answer_display");

    answer_option_list.innerHTML = "";
    user_answer_display.innerHTML = "";
    correct_answer_display.innerHTML = "";

    let currentAnswerObject = filteredQuestions[currentAnswer];

    answer_question.innerText = currentAnswerObject.question;
    current_answer.innerText = currentAnswer + 1;
    total_answer.innerText = filteredQuestions.length;

    for (let i = 0; i < currentAnswerObject.option.length; i++) {
        let optiondiv = document.createElement("div");
        optiondiv.setAttribute("class", "optiondiv");

        let labelElement = document.createElement("label");
        labelElement.innerText = currentAnswerObject.option[i];
        labelElement.setAttribute("class", "ms-2");

        if (userAnswers[currentAnswer] === currentAnswerObject.option[i]) {
            optiondiv.style.backgroundColor = currentAnswerObject.option[i] === currentAnswerObject.answer ? "green" : "red";
        }

        optiondiv.appendChild(labelElement);
        answer_option_list.appendChild(optiondiv);
    }

    if (userAnswers[currentAnswer]) {
        user_answer_display.innerText = `Your Answer: ${userAnswers[currentAnswer]}`;
        user_answer_display.style.color =
            userAnswers[currentAnswer] === currentAnswerObject.answer ? "green" : "red";

        answer_question.style.color =
            userAnswers[currentAnswer] === currentAnswerObject.answer ? "white" : "red";
    } else {
        user_answer_display.innerText = "Your Answer: No Answer Selected";
        user_answer_display.style.color = "red";
        answer_question.style.color = "red";
    }

    correct_answer_display.innerText = `Correct Answer: ${currentAnswerObject.answer}`;
    correct_answer_display.style.color = "green";
}

function nextAnswer() {
    if (currentAnswer < filteredQuestions.length - 1) {
        currentAnswer++;
        loadAnswers();
    } else {
        Swal.fire({
            icon: 'info',
            title: 'End of Answers',
            text: 'You have reviewed all the answers.',
            timer: 2000,
            showConfirmButton: false
        });
        setTimeout(() => {
            restartQuiz();
        }, 2000);
    }
}

function restartQuiz() {
    currentQuestion = 0;
    currentAnswer = 0;
    score = 0;
    document.getElementById("result_container").style.display = "none";
    document.getElementById("answers_container").style.display = "none";
    document.getElementById("main_container").style.display = "block";
    username.value = "";
    topic.value = "";
}
